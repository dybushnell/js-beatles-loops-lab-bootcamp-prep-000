function theBeatlesPlay(mus, inst){
  
  var firstArray = [];
  var i;
  
  for(i = 0; i < mus.length; i++){
   var firstArrayContents = (mus[i] + " plays " + inst[i])
   firstArray = [...firstArray, firstArrayContents]
   return firstArray
  }
}

